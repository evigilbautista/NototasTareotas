@Composable
fun EditTextInBox() {
    var text by remember { mutableStateOf(TextFieldValue()) }

    Box(
        contentAlignment = Alignment.BottomCenter,
        modifier = Modifier
            .fillMaxSize()
    ) {
        BasicTextField(
            value = text,
            onValueChange = {
                text = it
            },

            modifier = Modifier
                .fillMaxWidth()
                .background(Color.White)
                .padding(16.dp)
        )
    }
}

